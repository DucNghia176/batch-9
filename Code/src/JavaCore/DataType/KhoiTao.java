package JavaCore.DataType;

public class KhoiTao {
    byte aByte;
    short aShort;
    int aInt;
    long aLong;
    float aFloat;
    double aDouble;
    boolean aBoolean;
    char aChar;

    String aString;
    Integer aInteger;
    public static void main(String[] args) {
        KhoiTao dv = new KhoiTao();

        System.out.println("byte: " + dv.aByte);
        System.out.println("short: " + dv.aShort);
        System.out.println("int: " + dv.aInt);
        System.out.println("long: " + dv.aLong);
        System.out.println("float: " + dv.aFloat);
        System.out.println("double: " + dv.aDouble);
        System.out.println("boolean: " + dv.aBoolean);
        System.out.println("char: " + dv.aChar);
        System.out.println("String: " + dv.aString);
        System.out.println("Integer: " + dv.aInteger);
    }
}
